#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

int		main(int argc, char *argv[])
{
	int i;
	int a;

	a = argc - 1;
	i = 1;
	while (a > 0)
	{
		ft_putstr(argv[a]);
		ft_putchar('\n');
		a--;
	}
	return (0);
}
